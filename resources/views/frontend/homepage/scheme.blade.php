<script>
document.addEventListener("DOMContentLoaded", function() {
    const sliderContainer = document.querySelector('.slider-container');

    // Function to check if an element is in the viewport
    function isInViewport(element) {
        const rect = element.getBoundingClientRect();
        return (
            rect.top >= 0 &&
            rect.left >= 0 &&
            rect.bottom <= (window.innerHeight || document.documentElement.clientHeight) &&
            rect.right <= (window.innerWidth || document.documentElement.clientWidth)
        );
    }

    // Function to handle animation
    function handleAnimation() {
        if (isInViewport(sliderContainer)) {
            // Add slide-in animation class
            document.querySelectorAll('.r1').forEach(function(element) {
                element.classList.add('slide-in-from-left');
            });


            // Remove the event listener to prevent multiple triggers
            window.removeEventListener('scroll', handleAnimation);
        }
    }

    // Add event listener to handle animation when the section is scrolled into view
    window.addEventListener('scroll', handleAnimation);
});

</script>



